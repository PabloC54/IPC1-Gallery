    /**
     * 
     * @param img Imagen para redimensionar
     * @return Imagen redimensionada para encajar en un visor de imagenes
     */
    public int[] redimensionarImagen(BufferedImage img) {

        int ancho = 0, alto = 0, diferencia = 0;

        if (img.getWidth() <= 528 && img.getHeight() <= 348) { // Ambas dimensiones son más pequeñas
            ancho = 528;
            diferencia = 528 - img.getWidth();
            alto = img.getHeight() + diferencia * img.getHeight() / img.getWidth();

            if (alto > 348) {
                alto = 348;
                diferencia = img.getHeight() - 348;
                ancho = img.getWidth() - diferencia * img.getWidth() / img.getHeight();
            }
        } else {
            if (img.getWidth() > 528 && img.getHeight() > 348) {  // Ambas dimensiones son más grande
                ancho = 528;
                diferencia = img.getWidth() - 528;
                alto = img.getHeight() - diferencia * img.getHeight() / img.getWidth();

                if (alto > 348) {
                    alto = 348;
                    diferencia = img.getHeight() - 348;
                    ancho = img.getWidth() - diferencia * img.getWidth() / img.getHeight();
                }

            } else {
                if (img.getWidth() > 528) {  // El ancho es más grande
                    ancho = 528;
                    diferencia = img.getWidth() - 528;
                    alto = img.getHeight() - diferencia * img.getHeight() / img.getWidth();

                } else {  //El alto es más grande
                    alto = 348;
                    diferencia = img.getHeight() - 348;
                    ancho = img.getWidth() - diferencia * img.getWidth() / img.getHeight();
                }
            }
        }
        int[] dimensiones = {ancho, alto};
        return dimensiones;
    }


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Boton Añadir Imagen
        Varc.setCurrentDirectory(new File("C:\\Users\\pablo\\Documents\\NetBeansProjects\\Proyecto2\\src\\main\\java\\image"));
        int seleccion = Varc.showOpenDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION) { // Cuando se presiona aceptar

            imagenseleccionada=true;
            archivo = Varc.getSelectedFile();
            jLabel1.setText(archivo.getAbsolutePath());
            try {
                BufferedImage img = ImageIO.read(archivo);
                Image image = img.getScaledInstance(redimensionarImagen(img)[0], redimensionarImagen(img)[1], SCALE_SMOOTH);
                ImageIcon icon = new ImageIcon(image);
                jLabel7.setIcon(icon);
            } catch (IOException ex) {
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Boton EJECUTAR
        if (jRadioButton1.isSelected() || jRadioButton2.isSelected() || jRadioButton3.isSelected() || jRadioButton4.isSelected() || jRadioButton5.isSelected()) {
            if (imagenseleccionada == true) {
                imagenseleccionada = false;
                jLabel7.setIcon(new ImageIcon("C:\\Users\\pablo\\Documents\\NetBeansProjects\\Proyecto2\\src\\main\\java\\image\\default.jpg"));
                jLabel1.setText("");
                jLabel8.setText("");

                JPEGHandler handler;
                if (jRadioButton1.isSelected()) { // JPEG A BMP Y VICEVERSA 
                    try {
                        if (bmp == true) {
                            handler = new JPEGHandler(new BMPtoJPEGImage(archivo));
                        } else {
                            handler = new JPEGHandler(new JPEGtoBMPImage(archivo));
                        }
                    } catch (Exception ex) {
                        System.out.println("No se encontró el archivo");
                    }
                }
                if (jRadioButton2.isSelected() && archivo.getName().endsWith(".jpg")) { // COPIA JPEG
                    if (archivo.getName().endsWith(".jpg")) {
                        try {
                            handler = new JPEGHandler(new JPEGImageCopy(archivo));
                        } catch (Exception ex) {
                            System.out.println("No se encontró el archivo");
                        }
                    } else {
                        jLabel8.setText("Ingresa una imagen de formato JPEG");
                    }
                }
                if (jRadioButton3.isSelected()) { // ROJO, VERDE, AZUL, SEPIA
                    try {
                        handler = new JPEGHandler(new JPEGImageHandlerColors(archivo));
                    } catch (Exception ex) {
                        System.out.println("No se encontró el archivo");
                    }
                }
                if (jRadioButton4.isSelected()) { // MODIFICAR IMAGEN
                    try {
                        handler = new JPEGHandler(new JPEGImageHandlerRotator(archivo));
                    } catch (Exception ex) {
                        System.out.println("No se encontró el archivo");
                    }
                }
                if (jRadioButton5.isSelected()) { // BLANCO Y NEGRO
                    try {
                        handler = new JPEGHandler(new JPEGImageHandlerBN(archivo));
                    } catch (Exception ex) {
                        System.out.println("No se encontró el archivo");
                    }
                }
            } else {
                jLabel8.setText("Selecciona una imagen");
            }
        } else
            jLabel8.setText("Selecciona una opción");
    }//GEN-LAST:event_jButton1ActionPerformed

